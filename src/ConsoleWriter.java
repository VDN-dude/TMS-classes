import java.math.BigDecimal;

public class ConsoleWriter {
    void writeln(String message) {System.out.println(message);}
    void writeln(double number) {System.out.println(number);}
    void writeln(int number) {System.out.println(number);}
    void writeln(BigDecimal number) {System.out.println(number);}
    void write(String message) {System.out.print(message);}
    void write(double number) {System.out.print(number);}
    void write(int number) {System.out.print(number);}
    void write(BigDecimal number) {System.out.print(number);}
}
